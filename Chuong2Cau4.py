# 1. Số nguyên (Integer)
x = 5
y = -3
print(f"Số nguyên: {x}, {y}")

# 2. Số thực (Floating Point)
pi = 3.14
temperature = -0.001
print(f"Số thực: {pi}, {temperature}")

# 3. Chuỗi (String)
name = "Python"
greeting = 'Hello, World!'
print(f"Chuỗi: {name}, {greeting}")

# 4. Danh sách (List)
fruits = ["apple", "banana", "cherry"]
numbers = [1, 2, 3, 4, 5]
print(f"Danh sách: {fruits}, {numbers}")

# 5. Tuple
coordinates = (10.5, 20.3)
colors = ("red", "green", "blue")
print(f"Tuple: {coordinates}, {colors}")

# 6. Từ điển (Dictionary)
person = {"name": "John", "age": 25, "city": "New York"}
print(f"Từ điển: {person}")

# 7. Tập hợp (Set)
unique_numbers = {1, 2, 3, 4, 5}
fruits_set = {"apple", "banana", "cherry"}
print(f"Tập hợp: {unique_numbers}, {fruits_set}")

# 8. Boolean
is_active = True
is_sunny = False
print(f"Boolean: {is_active}, {is_sunny}")

# 9. None
nothing = None
print(f"None: {nothing}")

i1 = 2
i2 = 5
i3 = -3
d1 = 2.0
d2 = 5.0
d3 = -0.5

print("(a)", i1 + (i2 * i3))        # 2 + (5 * -3) = 2 + (-15) = -13
print("(b)", i1 * (i2 + i3))        # 2 * (5 + -3) = 2 * 2 = 4
print("(c)", i1 / (i2 + i3))        # 2 / (5 + -3) = 2 / 2 = 1.0 (chia thực)
print("(d)", i1 // (i2 + i3))       # 2 // 2 = 1 (chia lấy phần nguyên)
print("(e)", i1 / i2 + i3)          # (2 / 5) + (-3) = 0.4 - 3 = -2.6
print("(f)", i1 // i2 + i3)         # (2 // 5) + (-3) = 0 + (-3) = -3
print("(g)", 3 + 4 + 5 / 3)         # 3 + 4 + (5/3) = 3 + 4 + 1.666... = 8.666...
print("(h)", 3 + 4 + 5 // 3)        # 3 + 4 + (5//3) = 3 + 4 + 1 = 8
print("(i)", (3 + 4 + 5) / 3)       # (3+4+5)/3 = 12/3 = 4.0
print("(j)", (3 + 4 + 5) // 3)      # 12 // 3 = 4
print("(k)", d1 + (d2 * d3))        # 2.0 + (5.0 * -0.5) = 2.0 + (-2.5) = -0.5
print("(l)", d1 + d2 * d3)          # 2.0 + 5.0 * -0.5 = 2.0 - 2.5 = -0.5 (như trên)
print("(m)", d1 / d2 - d3)          # (2.0 / 5.0) - (-0.5) = 0.4 + 0.5 = 0.9
print("(n)", d1 / (d2 - d3))        # 2.0 / (5.0 - (-0.5)) = 2.0 / 5.5 = 0.3636363636...
print("(o)", d1 + d2 + d3 / 3)      # 2.0 + 5.0 + (-0.5 / 3) = 7.0 - 0.166666... = 6.8333...
print("(p)", (d1 + d2 + d3) / 3)    # (2.0 + 5.0 - 0.5) / 3 = 6.5 / 3 = 2.166666...
print("(q)", d1 + d2 + (d3 / 3))    # Giống (o): 6.8333...
print("(r)", 3 * (d1 + d2) * (d1 - d3))  
